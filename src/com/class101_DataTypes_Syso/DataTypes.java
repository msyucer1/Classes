package com.class101_DataTypes_Syso;

public class DataTypes {

	public static void main(String[] args) {

		
		// numeric family
		
		Short s = 12745;
		
		byte b = 126;
		
		int i = 823648;
		
		long m = 734723478783272365l;
		
		// decimal family
		float f = 2.1f;
		//when i use "f" at the end of the value, it accepts my value. But if i don't it doesn't.
		double d = 2.98734587346598437599128643118723687213;
		
		// letter
		char a = 'b';
		
		//true false
		boolean n = false;
		
		//write whatever you want
		String str ="hey guys!";
				
		System.out.println(str);
		System.out.println(b);
		System.out.println(m);
		System.out.println(a);
		System.out.println(n);
		System.out.println(d);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		
		
	}

}
